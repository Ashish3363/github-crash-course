public class sum {
    public static int add(int a, int b) {
        return (a + b);
    }

    public static int addthreenumber(int a, int b, int c) {
        return (a + b + c);
    }

    public static int addfournumber(int a, int b, int c, int d) {
        return (a + b + c + d);
    }

    public static int addfivenumber(int a, int b, int c, int d, int e) {
        return (a + b + c + d + e);
    }

    public static void main(String[] args) {
        System.out.println("My name is Ashish");
        System.out.println("Ashish Kumar");
    }
}
